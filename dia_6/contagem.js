let inteiro = Number(prompt("Digite um número"));
console.log("Até o número dado");
for(let i = 0; i <= inteiro; i++){
    console.log(i);
};
console.log("De 5 em 5");
for(let i = 0; i <= 50; i+=5){
    console.log(i);
};
console.log("De 5 em 5 invertido");
for(let i = 50; i >= 0; i-=5){
    console.log(i);
};